      <p class="orange-heading color-orange">Approach:</p>
      <p class="case_study-small-copy">In an attempt to turn the challenging situation into an opportunity to connect with the fans, we came up with the #PaltanSobatAarti Contest. It was a fun contest where every day from 17th August - 21st August, one question about Ganesh Utsav was posted and one lucky participant with the correct answer was selected as the winner everyday.<br><br>
The winners got a chance to host the player for a virtual Ganesh Aarti at their homes. On a Zoom video call, the player interacted with the fan and his/her family followed by a virtual Ganesh Aarti. To add cherry to the cake, all the sessions were recorded and short videos were created and shared with the followers across the brand's social media platforms.
      </p>
