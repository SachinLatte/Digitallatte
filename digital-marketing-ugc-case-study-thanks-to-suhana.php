    <div class="other-content">
      <p class="orange-heading color-orange">Approach:</p>
      <p class="case_study-small-copy">
With the idea to amplify customer feedback, <span style="color:#ff9000;">#ThanksToSuhana</span> was launched on Suhana Masala’s <a href="https://www.facebook.com/SuhanaTheTastemakers" target="_blank" style="color:blue; text-decoration:none;font-weight:bold;">Facebook</a> & <a href="https://www.instagram.com/suhanataste/" target="_blank" style="color:blue; text-decoration:none;font-weight:bold;">Instagram</a> pages.<br><br>

In the first phase of the campaign, we came up with a series of posts that showcased customer feedback rhyming with the campaign thought <span style="color:#ff9000;">#ThanksToSuhana.</span> For example, "Hostel mein mila ghar jaisa khana, Thanks to Suhana."<br><br>

This set the tone for the campaign and also lent a voice to original customer feedback. The fans loved the content series; it resulted in a 30% increase in post reach and engagement.<br><br>
The second phase of the campaign was a UGC contest, where the fans were asked to put their creative hats on and rhyme their cooking-based thoughts with <span style="color:#ff9000;">#ThanksToSuhana.</span> The top 5 entries were featured on Suhana Masala’s official social media pages, and they also won Suhana hampers; the fans loved the concept, the incentivization and their content being featured on the brand pages, and they participated in large numbers. The overwhelming response resulted in soaring engagement levels and increased follower count.<br><br>

In the end, it was a win-win situation for the brand and the fans. The brand leveraged User Generated Content (UGC) to connect with their fans, amplify customer feedback, drive engagement and increase followers. While on the other hand, the fans cherished being a part of the brand story along while also winning gift hampers.<br><br>

This was one campaign that left us feeling, 'Mazaa aa gaya'.
</p>
