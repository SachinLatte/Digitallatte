      <p class="orange-heading color-orange">Approach:</p>
      <p class="case_study-small-copy">The #IndianFoodLeague campaign conceptualised and executed by Goldiee Masale, launched a <a href="https://digitallatte.in/index" style="text-decoration:none;color:#e07f2a;font-weight:600;">Digital Latte</a> intended to match the momentum of the IPL 2020 matches and connect it with different food dishes and <a href="https://goldiee.com/" target="_blank" style="text-decoration:none;color:#e07f2a;font-weight:600;">Goldiee Masale products</a>. <br><br>

The four phased campaign started by connecting daily matches with Goldiee Products through witty one-liners and powerful designs. The concept and IPL style lingo like Pav Bhaji DilSe, Sunriser or Knight Rider - One One Noodles for all, Chhole Bhature - The choice of Capital captured user's attention and helped the brand join the IPL bandwagon.<br><br>

The phase two introduced different food dishes as trump cards highlighting IPL style key features like home team, partnership, Special Skills, Wins etc.<br><br>

The phase 3 saw an introduction of match winning recipes using Goldiee Masale to add delight to your match viewing experience at home.<br><br>

Lastly to keep the buzz alive and the excitement level, the brand ran an IPL match related fun contest every Friday where 5 winners got a chance to win the Goldiee Hamper. <br><br>

The swash buckling and flamboyant campaign set the perfect tone for Goldiee Masale for the festive season ahead.
</p>
