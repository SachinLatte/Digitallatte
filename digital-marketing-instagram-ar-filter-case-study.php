    <div class="other-content">
      <p class="orange-heading color-orange">Approach:</p>
      <p class="case_study-small-copy">
      We came up with a unique AR filter on <a href="https://www.instagram.com/puneripaltanofficial/?utm_source=ig_profile_share&igshid=m2wsuxrbs1f8" style="color:blue; text-decoration:none; font-weight:bold;" target="_blank">Instagram</a> for Puneri Paltan. All that the fans had to do was go to Puneri Paltan's Instagram profile, click on 'Try Filter' and click their own picture to post it on their Insta Story. A customized Pheta (Turban) appeared over the head of anyone using the AR filter. The saffron turban promoted the charm of our culture, the roots of Pune's heritage, while being tied to the brand Puneri Paltan and its sportsmanship charisma. <br><br>

    To kickstart the campaign, Paltan players put their turbaned AR filter selfies on their Instagram profiles. Fans took to it in no time and started using the filters too. We added more sparkle to the campaign by announcing that 5 lucky fans stood a chance to win Team Jersey. All the fans had to do was click a selfie using the filter, post it on their profile tagging Puneri Paltan, and then tag their friends too. We shared every contestant’s story on the Puneri Paltan page so as to encourage further participation.<br><br>

    Fans loved the entire experience and even put up their Turbaned pictures as their Profile pictures on their <a href="https://www.facebook.com/puneripaltan/" style="color:blue; text-decoration:none; font-weight:bold;" target="_blank">Facebook</a>, <a href="https://www.instagram.com/puneripaltanofficial/?utm_source=ig_profile_share&igshid=m2wsuxrbs1f8" style="color:blue; text-decoration:none; font-weight:bold;" target="_blank">Instagram</a> & <a href="https://web.whatsapp.com/" style="color:blue; text-decoration:none; font-weight:bold;" target="_blank">WhatsApp</a> profiles. The AR filter thus made a home in the hearts of the Puneri Paltan fan army. The fans could celebrate the festivals with their favorite players despite being home. There couldn’t have been a better win for the team during a muted lockdown Diwali.<br><br>

    Owing to the huge success of this <a href="https://digitallatte.in/our-expertise/digital-marketing-services/Social-Media-Marketing" style="color:blue; text-decoration:none; font-weight:bold;" target="_blank">Social Media campaign</a>, we decided to extend it to Christmas celebrations as well, with a little tweak. We replaced the turbans with Santa hats for Christmas and witnessed a fabulous fan participation in use of the Christmas AR filter as well.

</p>
